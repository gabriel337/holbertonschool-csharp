using System;

namespace MyMath
{
    /// <summary>
    /// Basic math operation
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// sums two numbers
        /// </summary>
        /// <param name="a">negative or positive number</param>
        /// <param name="b">negative or positive number</param>
        /// <returns>sum result</returns>
        public static int Add(int a, int b)
        {
            return (a + b);
        }
    }
}